	constexpr Word GMixColumnInverse(const Word& in) noexcept
	{
		Word r;
		r[0] = GMulBy14[in[0]] ^ GMulBy9[in[3]] ^ GMulBy13[in[2]] ^ GMulBy11[in[1]];
		r[1] = GMulBy14[in[1]] ^ GMulBy9[in[0]] ^ GMulBy13[in[3]] ^ GMulBy11[in[2]];
		r[2] = GMulBy14[in[2]] ^ GMulBy9[in[1]] ^ GMulBy13[in[0]] ^ GMulBy11[in[3]];
		r[3] = GMulBy14[in[3]] ^ GMulBy9[in[2]] ^ GMulBy13[in[1]] ^ GMulBy11[in[0]];
		return r;
	}

